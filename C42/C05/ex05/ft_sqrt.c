#include<stdio.h>

int ft_sqrt(int nb)
{
	int	i;
	i = 0;

	while (i * i < nb && i < 46340)
		i++;
	if (i * i == nb)
		return (i);
}

int main()
{
	printf("%d",ft_sqrt(1024));
}
